    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

       // Check if the key text field is empty
    if (txtClave.getText().trim().isEmpty()) {
        // Display an alert message
        JOptionPane.showMessageDialog(null, "Please enter a value in the key field before searching.");
        return; // Exit the method to prevent further execution
    }

    String keyToSearch = txtClave.getText().trim();

    try {
        // Reading the file and looking for the information
        File inputFile = new File("person.txt");
        BufferedReader reader = new BufferedReader(new FileReader(inputFile));

        String currentLine;
        boolean found = false;

        while ((currentLine = reader.readLine()) != null) {
            String[] parts = currentLine.split(",");
            if (parts.length > 0 && parts[0].equals(keyToSearch)) { // taking in consideration that the key is the first field 
                // filling the fields with text with the found information 
                txtId.setText(parts[0]); 
                txtName.setText(parts[1]);
                txtLastName.setText(parts[2]);
                txtCountry.setText(parts[3]);
                txtPhone.setText(parts[4]);
                txtEmail.setText(parts[5]);
                txtBirth.setText(parts[6]);
                cbxGenero.setSelectedItem(parts[7]); 
                found = true;
                break; //
            }
        }

        reader.close();

        if (!found) {
            JOptionPane.showMessageDialog(null, "There's no one with that key here");
        }

    } catch (IOException e) {
        System.err.println("Error reading file: " + e.getMessage());
    }

    
    btnSave.setEnabled(!txtClave.getText().isEmpty());
    }//GEN-LAST:event_btnSearchActionPerformed

