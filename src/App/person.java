    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

         if (txtName.getText().isEmpty() || txtClave.getText().isEmpty() ||
        txtLastName.getText().isEmpty() || txtCountry.getText().isEmpty() ||
        txtPhone.getText().isEmpty() || txtEmail.getText().isEmpty() ||
        txtBirth.getText().isEmpty() || cbxGenero.getSelectedItem() == null) {

        JOptionPane.showMessageDialog(null, "Please, complete all the information");
        return;
    }

    // Creating a String with the information to be saved
    String data = String.join(",", 
        txtClave.getText(), 
        txtName.getText(), 
        txtLastName.getText(), 
        txtCountry.getText(), 
        txtPhone.getText(), 
        txtEmail.getText(), 
        txtBirth.getText(), 
        cbxGenero.getSelectedItem().toString()
    );

    try {
        // Escribir en un archivo
        FileWriter fw = new FileWriter("person.txt", true); // 'true' to add at the end of the file
        BufferedWriter bw = new BufferedWriter(fw);
        bw.write(data);
        bw.newLine(); // Add a new line
        bw.close();

        JOptionPane.showMessageDialog(null, "Saved Person");
        limpiarCajas();

    } catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Error saving person: " + e.getMessage());
    }

    btnSave.setEnabled(false);
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        // TODO add your handling code here:

         if (txtName.getText().isEmpty() || txtClave.getText().isEmpty() ||
        txtLastName.getText().isEmpty() || txtCountry.getText().isEmpty() ||
        txtPhone.getText().isEmpty() || txtEmail.getText().isEmpty() ||
        txtBirth.getText().isEmpty() || cbxGenero.getSelectedItem() == null ||
        txtId.getText().isEmpty()) {

        JOptionPane.showMessageDialog(null, "Please, complete all the information");
        return;
    }

    String idToUpdate = txtId.getText();
    String newData = String.join(",", 
        txtClave.getText(), 
        txtName.getText(), 
        txtLastName.getText(), 
        txtCountry.getText(), 
        txtPhone.getText(), 
        txtEmail.getText(), 
        txtBirth.getText(), 
        cbxGenero.getSelectedItem().toString()
    );

    try {
        // read the file and update the information
        File inputFile = new File("person.txt");
        File tempFile = new File("temp_personas.txt");

        BufferedReader reader = new BufferedReader(new FileReader(inputFile));
        BufferedWriter writer = new BufferedWriter(new FileWriter(tempFile));

        String currentLine;
        boolean updated = false;

        while ((currentLine = reader.readLine()) != null) {
            String[] parts = currentLine.split(",");
            if (parts.length > 0 && parts[0].equals(idToUpdate)) { // taking in consideration that the ID is the first field
                writer.write(newData);
                updated = true;
            } else {
                writer.write(currentLine);
            }
            writer.newLine();
        }

        writer.close();
        reader.close();

        // Replacing the original file with the temporal file 
        if (inputFile.delete()) {
            tempFile.renameTo(inputFile);
        }

        if (updated) {
            JOptionPane.showMessageDialog(null, "Modified person");
            limpiarCajas();
        } else {
            JOptionPane.showMessageDialog(null, "Error modifying: ID not found");
        }

    } catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Error modifying: " + e.getMessage());
    }

    btnModify.setEnabled(false);
     btnSave.setEnabled(false);

    }//GEN-LAST:event_btnModifyActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (txtId.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please, enter an ID to delete");
        return;
    }

    String idToDelete = txtId.getText();

    try {
        // Reading the file and deleting the information 
        File inputFile = new File("person.txt");
        File tempFile = new File("temp_personas.txt");

        BufferedReader reader = new BufferedReader(new FileReader(inputFile));
        BufferedWriter writer = new BufferedWriter(new FileWriter(tempFile));

        String currentLine;
        boolean deleted = false;

        while ((currentLine = reader.readLine()) != null) {
            String[] parts = currentLine.split(",");
            if (parts.length > 0 && parts[0].equals(idToDelete)) { // taking in consideration that the ID is the first field
                deleted = true; // We alert that it's been deleted 
                continue; // We don't write this line in the temporal file
            }
            writer.write(currentLine);
            writer.newLine();
        }

        writer.close();
        reader.close();

        // Replacing the original file with the temporal file 
        if (inputFile.delete()) {
            tempFile.renameTo(inputFile);
        }

        if (deleted) {
            JOptionPane.showMessageDialog(null, "Deleted person");
            limpiarCajas();
        } else {
            JOptionPane.showMessageDialog(null, "Error deleting person: ID not found");
        }

    } catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Error deleting person: " + e.getMessage());
    }

    btnDelete.setEnabled(false);
    }//GEN-LAST:event_btnDeleteActionPerformed

